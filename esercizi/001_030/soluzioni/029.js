// Scrivere un'espressione che calcola la media pesata di tre numeri.

// Dichiarazione delle variabili `a`, `b`, `c` e dei loro pesi `p1`, `p2`, `p3`
let a = 5, p1 = 1;
let b = 10, p2 = 2;
let c = 15, p3 = 3;

// Pi√π variabili possono essere dichiarate sulla stessa riga separandole con la virgola, come sopra.

// Calcolo della media pesata
let mediaPesata = (a * p1 + b * p2 + c * p3) / (p1 + p2 + p3);

// Stampa della media pesata in console
console.log(mediaPesata);
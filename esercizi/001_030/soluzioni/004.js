// Creare una variabile `eta` e controllare se è maggiore o uguale a 18.

// Dichiarazione della variabile `eta` e assegnazione del valore 20
let eta = 20;

// Controllo se `eta` è maggiore o uguale a 18
let maggiorenne = eta >= 18;

// eta >= 18 è un'espressione booleana che restituisce true o false
// Quindi `maggiorenne` sarà true se `eta` è maggiore o uguale a 18, false altrimenti

// Stampa del risultato del controllo in console
console.log(maggiorenne);
// Usare `Math.pow()` per elevare un numero a una potenza.

// Dichiarazione delle variabili `base` e `esponente` e assegnazione dei valori 2 e 3
let base = 2;
let esponente = 3;

// Calcolo della potenza usando `Math.pow()`
let potenza = Math.pow(base, esponente);

// Stampa del risultato in console
console.log(potenza);
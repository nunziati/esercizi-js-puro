// Incrementare una variabile di 1 con `++`.

// Dichiarazione della variabile `numero` e assegnazione del valore 10
let numero = 10;

// Incremento della variabile `numero` di 1
numero++;

// Esiste anche l'operatore `--` per decrementare la variabile di 1
// numero--;

// Stampa del valore incrementato in console
console.log(numero);
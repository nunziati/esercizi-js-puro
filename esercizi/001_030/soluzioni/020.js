// Contare il numero di cifre di un numero intero.

// Dichiarazione della variabile `numero` e assegnazione del valore 12345
let numero = 12345;

// Conversione del numero in stringa e conteggio delle cifre
let numeroCifre = numero.toString().length;

// length è una proprietà di una stringa che restituisce il numero di caratteri
// Anche altri tipi di dati hanno length, come ad esempio gli array

// Stampa del numero di cifre in console
console.log(numeroCifre);
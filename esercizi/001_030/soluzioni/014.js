// Controllare se un numero è positivo, negativo o zero, senza usare if/else if/else.

// Dichiarazione della variabile `numero` e assegnazione del valore -5
let numero = -5;

// Controllo se il numero è positivo, negativo o zero usando operatori ternari
let risultato = (numero > 0) ? "positivo" : ( (numero < 0) ? "negativo" : "zero" );

// Stampa del risultato in console
console.log(risultato);

// Verificare se un numero è un multiplo di un altro.

// Dichiarazione delle variabili `numero` e `multiplo` e assegnazione dei valori 20 e 5
let numero = 20;
let multiplo = 5;

// Controllo se `numero` è un multiplo di `multiplo`
let eMultiplo = numero % multiplo === 0;

// Stampa del risultato del controllo in console
console.log(eMultiplo);